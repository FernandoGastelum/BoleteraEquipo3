    private void BotonConfirmar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConfirmar
         try {
            double saldoAAgregar = Double.parseDouble(jTextField2.getText());
            String correo = jTextField3.getText();
            double nuevoSaldo = saldoActual + saldoAAgregar;

            if (usuarioDAO.actualizarSaldoPorCorreo(correo, nuevoSaldo)) {
                saldoActual = nuevoSaldo;
                mostrarSaldo(correo);
                JOptionPane.showMessageDialog(this, "Saldo actualizado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar el saldo.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor válido para el saldo.");
        }
    }//GEN-LAST:event_BotonConfirmar

