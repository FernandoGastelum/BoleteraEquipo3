    private void mostrarSaldo(String correoUsuario) {
    // Obtener el saldo según el correo del usuario
    saldoActual = usuarioDAO.obtenerSaldoPorCorreo(correoUsuario);
    if (saldoActual >= 0) {
        jTextField1.setText(String.valueOf(saldoActual));
    } else {
        JOptionPane.showMessageDialog(this, "Usuario no encontrado o sin saldo.");
        jTextField1.setText("0.0");
    }
}

